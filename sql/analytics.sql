# Total Sales Per Category
SELECT
	CATEGORY,
	SUM(AMOUNT) AS TOTAL_SALES
FROM
	SALES_DATA
GROUP BY
	CATEGORY;

# Total Sales Per Category
SELECT
	CUSTOMER_NAME,
	SUM(AMOUNT) AS TOTAL_AMOUNT
FROM
	SALES_DATA
GROUP BY
	CUSTOMER_NAME
ORDER BY
	TOTAL_AMOUNT DESC;

# Total Sales Per Month
SELECT
	month,
	SUM(amount)
FROM
	sales
GROUP BY
	month;

# INCREMENTAL LOADING
SELECT
	*
FROM
	sales
WHERE
	updated_at > '2026-05-06';